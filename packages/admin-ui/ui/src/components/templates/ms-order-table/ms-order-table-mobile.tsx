import clsx from "clsx"
import { isEmpty } from "lodash"
import { useAdminOrders } from "medusa-react"
import qs from "qs"
import React, { useEffect, useState } from "react"
import { useLocation } from "react-router-dom"
import { usePagination, useTable } from "react-table"
import { useAnalytics } from "../../../providers/analytics-provider"
import useOrderTableColums from "./use-order-column"
import { useOrderFilters } from "./use-order-filters"
import MsTableContainer from "../../organisms/ms-table-container"
// import MsTable from "../../molecules/ms-table/ms-table-mobile"
import useOrderActions from "./use-order-action"
import { useRowSelect } from "react-table"
import IndeterminateCheckbox from "../../molecules/indeterminate-checkbox"
import TableEmptyState from "./table-empty-state"
import MsOrderFiltersMobile from "../ms-order-filter-dropdown/ms-order-filter-mobile"
import MsTableMobile from "../../molecules/ms-table/ms-table-mobile"

const DEFAULT_PAGE_SIZE = 15

const defaultQueryProps = {
  expand: "customer,shipping_address",
  fields:
    "id,status,display_id,created_at,email,fulfillment_status,payment_status,total,currency_code",
}

type OrderTableProps = {
  setContextFilters: (filters: Record<string, { filter: string[] }>) => void
}

const MsOrderTableMobile = ({ setContextFilters }: OrderTableProps) => {
  const location = useLocation()

  const { trackNumberOfOrders } = useAnalytics()

  const {
    removeTab,
    setTab,
    saveTab,
    availableTabs: filterTabs,
    activeFilterTab,
    reset,
    paginate,
    setFilters,
    filters,
    setQuery: setFreeText,
    queryObject,
    representationObject,
  } = useOrderFilters(location.search, defaultQueryProps)
  const filtersOnLoad = queryObject

  const offs = parseInt(filtersOnLoad?.offset) || 0
  const lim = parseInt(filtersOnLoad.limit) || DEFAULT_PAGE_SIZE

  const [query, setQuery] = useState(filtersOnLoad?.query)
  const [numPages, setNumPages] = useState(0)
  const [closeFilter, setCloseFilter] = useState(false)

  const { orders, isLoading, count } = useAdminOrders(queryObject, {
    keepPreviousData: true,
    onSuccess: ({ count }) => {
      trackNumberOfOrders({
        count,
      })
    },
  })

  useEffect(() => {
    const controlledPageCount = Math.ceil(count! / queryObject.limit)
    setNumPages(controlledPageCount)
  }, [orders])

  useEffect(() => {
    setContextFilters(filters as {})
  }, [filters])

  const [columns] = useOrderTableColums()
  const { getActions } = useOrderActions()

  const {
    getTableProps,
    getTableBodyProps,
    rows,
    prepareRow,
    canPreviousPage,
    canNextPage,
    headerGroups,
    pageCount,
    nextPage,
    gotoPage,
    previousPage,
    // Get the state from the instance
    state: { pageIndex, pageSize, selectedRowIds },
  } = useTable(
    {
      columns,
      data: orders || [],
      manualPagination: true,
      initialState: {
        pageIndex: 1,
        pageSize: 12,
        // selectedRowIds: orders && orders.reduce((prev, { id }) => {
        //   prev[id] = true
        //   return prev
        // }, {}),
      },
      pageCount: numPages,
      autoResetSelectedRows: false,
      autoResetPage: false,
      getRowId: (row) => row.id,
    },
    usePagination,
    useRowSelect,
    (hooks) => {
      hooks.visibleColumns.push((columns) => [
        // Let's make a column for selection
        {
          id: "selection",
          // The header can use the table's getToggleAllRowsSelectedProps method
          // to render a checkbox
          Header: ({ getToggleAllRowsSelectedProps }) => {
            return (
              <div className="pl-4">
                <IndeterminateCheckbox {...getToggleAllRowsSelectedProps()} />
              </div>
            )
          },
          // The cell can use the individual row's getToggleRowSelectedProps method
          // to the render a checkbox
          Cell: ({ row }) => {
            return (
              <div className="pl-4">
                <IndeterminateCheckbox {...row.getToggleRowSelectedProps()} />
              </div>
            )
          },
        },
        ...columns,
      ])
    }
  )

  // Debounced search
  useEffect(() => {
    const delayDebounceFn = setTimeout(() => {
      if (query) {
        setFreeText(query)
        gotoPage(0)
      } else {
        // if we delete query string, we reset the table view
        reset()
      }
    }, 400)

    return () => clearTimeout(delayDebounceFn)
  }, [query])

  const handleNext = () => {
    if (canNextPage) {
      paginate(1)
      nextPage()
    }
  }

  const handlePrev = () => {
    if (canPreviousPage) {
      paginate(-1)
      previousPage()
    }
  }

  const updateUrlFromFilter = (obj = {}) => {
    const stringified = qs.stringify(obj)
    window.history.replaceState(`/a/orders`, "", `${`?${stringified}`}`)
  }

  const refreshWithFilters = () => {
    const filterObj = representationObject

    if (isEmpty(filterObj)) {
      updateUrlFromFilter({ offset: 0, limit: DEFAULT_PAGE_SIZE })
    } else {
      updateUrlFromFilter(filterObj)
    }
  }

  const clearFilters = () => {
    reset()
    setQuery("")
  }

  useEffect(() => {
    refreshWithFilters()
  }, [representationObject])

  return (
    <div>
      <MsTableContainer
        isLoading={isLoading}
        hasPagination={orders?.length ? true : false}
        numberOfRows={lim}
        pagingState={{
          count: count!,
          offset: queryObject.offset,
          pageSize: queryObject.offset + rows.length,
          title: "Orders",
          currentPage: pageIndex + 1,
          pageCount: pageCount,
          nextPage: handleNext,
          prevPage: handlePrev,
          hasNext: canNextPage,
          hasPrev: canPreviousPage,
        }}
      >
        <MsTableMobile
          filteringOptions={
            <MsOrderFiltersMobile
              filters={filters}
              submitFilters={setFilters}
              clearFilters={clearFilters}
              tabs={filterTabs}
              onTabClick={setTab}
              activeTab={activeFilterTab}
              onRemoveTab={removeTab}
              onSaveTab={saveTab}
            />
          }
          enableSearch
          // searchDivClassName="!place-content-start"
          filteringDivClassName="my-0"
          searchPlaceholder="Search by order id, email, or customer name"
          handleSearch={setQuery}
          searchValue={query}
          {...getTableProps()}
          className={clsx({ ["relative"]: isLoading })}
          tableHeight="h-[calc(100vh-280px)] overflow-y-auto w-[100vw]"
        >
          <MsTableMobile.Head className="h-[64px]">
            {headerGroups?.map((headerGroup) => (
              <MsTableMobile.HeadRow
                {...headerGroup.getHeaderGroupProps()}
                className="bg-grey-100"
              >
                {headerGroup.headers.map((col) => (
                  <MsTableMobile.HeadCell {...col.getHeaderProps()}>
                    {col.render("Header")}
                  </MsTableMobile.HeadCell>
                ))}
                <MsTableMobile.HeadCell className="w-[80px]">
                  <span className="sr-only">Actions</span>
                </MsTableMobile.HeadCell>
              </MsTableMobile.HeadRow>
            ))}
          </MsTableMobile.Head>
          <MsTableMobile.Body {...getTableBodyProps()} className="shadow-none">
            {!orders ? (
              <TableEmptyState />
            ) : (
              rows.map((row) => {
                prepareRow(row)
                return (
                  <MsTableMobile.Row
                    color={"inherit"}
                    // linkTo={row.original.id}
                    {...row.getRowProps()}
                    className="group"
                    actions={getActions(row.original.id)}
                    clickable={true}
                  >
                    {row.cells.map((cell) => {
                      return (
                        <MsTableMobile.Cell {...cell.getCellProps()}>
                          {cell.render("Cell")}
                        </MsTableMobile.Cell>
                      )
                    })}
                  </MsTableMobile.Row>
                )
              })
            )}
          </MsTableMobile.Body>
        </MsTableMobile>
      </MsTableContainer>
    </div>
  )
}

export default React.memo(MsOrderTableMobile)
